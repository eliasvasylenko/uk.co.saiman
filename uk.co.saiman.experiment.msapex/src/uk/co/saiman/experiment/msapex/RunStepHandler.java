package uk.co.saiman.experiment.msapex;

import javax.inject.Inject;

import org.eclipse.e4.core.di.annotations.CanExecute;
import org.eclipse.e4.core.di.annotations.Execute;
import org.eclipse.e4.core.di.annotations.Optional;
import org.eclipse.e4.core.di.extensions.Service;

import uk.co.saiman.experiment.Step;
import uk.co.saiman.experiment.msapex.i18n.ExperimentProperties;
import uk.co.saiman.log.Log;

/**
 * Add an experiment to the workspace.
 * 
 * @author Elias N Vasylenko
 */
public class RunStepHandler {
  @Inject
  Log log;
  @Inject
  @Service
  ExperimentProperties text;

  @Execute
  void execute(Step step) {
    step.schedule();
    step.getExperiment().conduct();
  }

  @CanExecute
  boolean canExecute(@Optional Step step) {
    return step != null;
  }
}
